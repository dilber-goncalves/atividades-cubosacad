let x = 5, y = 2;

//soma
let resultado = x + y;
console.log(`A soma de ${x} e ${y} é = ${resultado}`);

//subtração
resultado = x - y;
console.log(`A subtração entre ${x} e ${y} é = ${resultado}`);

//multiplicação
resultado = x * y;
console.log(`A multiplicação de ${x} e ${y} é = ${resultado}`);

//divisão
resultado = x / y;
console.log(`A divisão de ${x} e ${y} é = ${resultado}`);